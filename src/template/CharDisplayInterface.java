package template;

public class CharDisplayInterface implements AbstractDisplayInterface{

    private char ch;

    public CharDisplayInterface(char ch) {
        this.ch = ch;
    }

    @Override
    public void open() {
        System.out.print("<<");
    }

    @Override
    public void print() {
        System.out.print(ch);
    }

    @Override
    public void close() {
        System.out.println(">>");
    }
}
